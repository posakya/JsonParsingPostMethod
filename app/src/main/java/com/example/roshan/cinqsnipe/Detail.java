public class Detail {

    private String id;
    private String type;
    private String purpose;
    private String featured_img;
    private String title;
    private String street_name;
    private String asking_price;
    private String floor_area;
    private String floor_area_unit;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPurpose() {
        return purpose;
    }

    public void setPurpose(String purpose) {
        this.purpose = purpose;
    }

    public String getFeatured_img() {
        return featured_img;
    }

    public void setFeatured_img(String featured_img) {
        this.featured_img = featured_img;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getStreet_name() {
        return street_name;
    }

    public void setStreet_name(String street_name) {
        this.street_name = street_name;
    }

    public String getAsking_price() {
        return asking_price;
    }

    public void setAsking_price(String asking_price) {
        this.asking_price = asking_price;
    }

    public String getFloor_area() {
        return floor_area;
    }

    public void setFloor_area(String floor_area) {
        this.floor_area = floor_area;
    }

    public String getFloor_area_unit() {
        return floor_area_unit;
    }

    public void setFloor_area_unit(String floor_area_unit) {
        this.floor_area_unit = floor_area_unit;
    }
}

